#!/usr/bin/env node
/**
 * Generate the Lambda cost publisher's pricing table from the pricing SSOT.
 *
 * The single source of truth for Bedrock model prices is
 * packages/pricing/src/data/bedrock-pricing.json — the same file the
 * TypeScript @eks-agent/pricing package imports. This script renders that
 * data into terraform/components/cost-pipeline/lambda/pricing_data.py so the
 * Lambda meters spend against the identical numbers, with no second
 * hand-maintained table to drift.
 *
 * Usage:
 *   node scripts/gen-lambda-pricing.mjs           # write pricing_data.py
 *   node scripts/gen-lambda-pricing.mjs --check    # exit 1 if it is stale
 *
 * CI runs --check so a change to the JSON that isn't regenerated (or a
 * hand-edit of the generated file) fails the build.
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(here, '..');
const SSOT = join(repoRoot, 'packages/pricing/src/data/bedrock-pricing.json');
const OUT = join(repoRoot, 'terraform/components/cost-pipeline/lambda/pricing_data.py');

/** Format a JSON number as a Python float literal (never an int). */
function pyFloat(n) {
  const s = Number.isInteger(n) ? `${n}.0` : `${n}`;
  if (s.includes('e') || s.includes('E')) {
    throw new Error(`refusing to emit non-decimal float literal: ${s}`);
  }
  return s;
}

function render() {
  const data = JSON.parse(readFileSync(SSOT, 'utf8'));
  const ids = Object.keys(data.models).sort();
  const rows = ids
    .map((id) => {
      const m = data.models[id];
      return `    ${JSON.stringify(id)}: {"input": ${pyFloat(m.inputPerMillion)}, "output": ${pyFloat(m.outputPerMillion)}},`;
    })
    .join('\n');
  return `# GENERATED by scripts/gen-lambda-pricing.mjs from
# packages/pricing/src/data/bedrock-pricing.json — DO NOT EDIT.
#
# Bedrock on-demand prices, USD per 1,000,000 tokens. This table is the Python
# projection of the pricing SSOT the TypeScript @eks-agent/pricing package
# imports; a CI drift gate (\`node scripts/gen-lambda-pricing.mjs --check\`)
# fails the build if the two diverge. To change a price, edit the JSON and
# regenerate — never hand-edit this file.

PRICING: dict[str, dict[str, float]] = {
${rows}
}
`;
}

const rendered = render();
const check = process.argv.includes('--check');

if (check) {
  let current = '';
  try {
    current = readFileSync(OUT, 'utf8');
  } catch {
    current = '';
  }
  if (current !== rendered) {
    // eslint-disable-next-line no-console
    console.error(
      'pricing_data.py is stale — regenerate with `node scripts/gen-lambda-pricing.mjs`\n' +
        'and commit the result (the Lambda price table must match the pricing SSOT).',
    );
    process.exit(1);
  }
  // eslint-disable-next-line no-console
  console.log('pricing_data.py is in sync with the pricing SSOT.');
} else {
  writeFileSync(OUT, rendered, 'utf8');
  // eslint-disable-next-line no-console
  console.log(`wrote ${OUT}`);
}
